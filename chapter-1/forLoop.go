package main

import (
	"fmt"
)

func main() {
	for i := 0; i < 10; i++ {
		fmt.Print(i*i, " ")
	}
	fmt.Println("\n-------------------------------------------------------------------------------------------------")

	i := 0
	for ok := true; ok; ok = (i != 10) {
		fmt.Print(i*i, " ")
		i++
	}
	fmt.Println("\n-------------------------------------------------------------------------------------------------")
	i = 0
	for {
		if i == 10 {
			break
		}
		fmt.Print(i*i, " ")
		i++
	}
	fmt.Println("\n-------------------------------------------------------------------------------------------------")
	aSlice := []int{-1, 2, 1, -1, 2, -2}
	for i, v := range aSlice {
		fmt.Println("Index:", i, "Value:", v)
	}

}
